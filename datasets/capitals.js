// Get all unique capitals for generating options
export function getAllCapitals() {
  return [...new Set(Object.values(capitals))];
}

// Get capital for a country code
export function getCapital(countryCode) {
  return capitals[countryCode] || null;
}

// Get random capital options (wrong answers)
export function getRandomCapitalOptions(correctCapital, count = 3) {
  const allCapitals = getAllCapitals();
  const options = [correctCapital];
  const available = allCapitals.filter(c => c !== correctCapital);

  const shuffled = [...available].sort(() => Math.random() - 0.5);
  for (let i = 0; i < count && i < shuffled.length; i++) {
    options.push(shuffled[i]);
  }

  return options.sort(() => Math.random() - 0.5);
}
